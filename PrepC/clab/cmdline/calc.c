#include <stdio.h>

int main (int argc, char *argv[])
{

	int val1 = atoi (argv[1]);
	int val2 = atoi (argv[3]);

	if (*argv[2] == '+')
		printf("%d", val1+val2);

	else if (*argv[2] == '-')
		printf("%d", val1-val2);

	else if (*argv[2] == '/')
		printf("%f", (float)val1/val2);

	else if (*argv[2] == '*')
		printf("%d", val1*val2);

	else 
		printf("Invalid input");

printf("\n");

}
